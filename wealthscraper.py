# ── Main Scraper ─────────────────────────────────────────────

class BankStatementScraper:

    def __init__(self, pdf_path: str, password: str = ""):
        self.pdf_path    = Path(pdf_path)
        self.password    = password
        self.raw_text    = ""
        self.bank_format = "unknown"   # "sbi" or "uco"
        self.summary     = FinancialSummary()

    # ── Step 1: Extract text (password aware) ────────────────
    def extract_raw_data(self):
        try:
            with pdfplumber.open(self.pdf_path, password=self.password) as pdf:
                for page in pdf.pages:
                    text = page.extract_text()
                    if text:
                        self.raw_text += text + "\n"
            print(f"  Text extracted ({len(self.raw_text)} chars)")

        except Exception:
            print("PDF is password protected.")
            print("""
Common bank statement passwords:
- Most banks  -> DDMMYYYY (date of birth)  e.g. 15081999
- SBI         -> First 5 letters of name + DOB  e.g. DHRUV23112005
- ICICI       -> First 4 letters of name + DOB  e.g. DHRU23112005
- UCO Bank    -> DDMMYYYY
""")
            unlocked = False
            for attempt in range(3):
                pwd = input(f"  Enter password (attempt {attempt+1}/3): ").strip()
                try:
                    with pdfplumber.open(self.pdf_path, password=pwd) as pdf:
                        for page in pdf.pages:
                            text = page.extract_text()
                            if text:
                                self.raw_text += text + "\n"
                    print(f"  Unlocked. ({len(self.raw_text)} chars extracted)")
                    unlocked = True
                    break
                except Exception:
                    print("  Wrong password, try again.")

            if not unlocked:
                raise ValueError(
                    "Could not unlock PDF.\n"
                    "Tip: Open PDF -> Print -> Save as PDF to remove password, then re-upload."
                )

    # ── Step 2: Detect bank format ───────────────────────────
    def detect_bank(self):
        text_upper = self.raw_text.upper()
        # Order matters: UCO statements often contain 'SBIN' in UPI refs,
        # so check for UCO Bank before SBI and only treat as SBI when clearly present.
        if "UCO BANK" in text_upper or "STATEMENT FOR ACCOUNT NUMBER" in text_upper and "UCO" in text_upper:
            self.bank_format = "uco"
            self.summary.bank_name = "UCO Bank"
        elif "STATE BANK OF INDIA" in text_upper:
            self.bank_format = "sbi"
            self.summary.bank_name = "State Bank of India"
        elif "HDFC" in text_upper:
            self.bank_format = "hdfc"
            self.summary.bank_name = "HDFC Bank"
        elif "ICICI" in text_upper:
            self.bank_format = "icici"
            self.summary.bank_name = "ICICI Bank"
        else:
            self.bank_format = "generic"
            self.summary.bank_name = "Unknown Bank"
        print(f"  Detected bank format: {self.bank_format.upper()}")

    # ── Step 3: Parse metadata ───────────────────────────────
    def parse_metadata(self):
        text = self.raw_text

        if self.bank_format == "sbi":
            self._parse_metadata_sbi(text)
        elif self.bank_format == "uco":
            self._parse_metadata_uco(text)
        else:
            self._parse_metadata_generic(text)

    def _parse_metadata_sbi(self, text):
        # Name: "Welcome: As on DD-MM-YYYY  NAME"
        name_m = re.search(r"Welcome:.*?(\d{2}-\d{2}-\d{4})\s+([A-Z][A-Z\s]{3,40})", text)
        if name_m:
            self.summary.account_holder = name_m.group(2).strip()

        # Account number
        acc_m = re.search(r"Account Number\s*:\s*(\d{10,18})", text, re.IGNORECASE)
        if acc_m:
            self.summary.account_number = acc_m.group(1)

        # Statement period: "Statement From :01-01-2026 to 31-01-2026"
        period_m = re.search(
            r"Statement From\s*:\s*(\d{2}-\d{2}-\d{4})\s+to\s+(\d{2}-\d{2}-\d{4})",
            text, re.IGNORECASE
        )
        if period_m:
            self.summary.statement_period_start = period_m.group(1)
            self.summary.statement_period_end   = period_m.group(2)

        # Opening balance = balance before first transaction
        # In SBI format, balance appears after the last amount on each line
        # The first balance value after header is opening
        balances = re.findall(r"([\d,]+\.\d{2})(?:CR|DR)?", text)
        # Clear Balance shown in header
        clear_bal = re.search(r"Clear Balance\s*:\s*([\d,]+\.\d{2})", text, re.IGNORECASE)
        if clear_bal:
            self.summary.closing_balance = self._to_float(clear_bal.group(1))

    def _parse_metadata_uco(self, text):
        acc_m = re.search(r"account number\s+(\d{10,18})", text, re.IGNORECASE)
        if acc_m:
            self.summary.account_number = acc_m.group(1)

        period_m = re.search(
            r"Between\s+(\d{2}-\d{2}-\d{4})\s+and\s+(\d{2}-\d{2}-\d{4})",
            text, re.IGNORECASE
        )
        if period_m:
            self.summary.statement_period_start = period_m.group(1)
            self.summary.statement_period_end   = period_m.group(2)

        name_m = re.search(r"^Name\s+([A-Z]+(?:\s+[A-Z]+){1,4})", text, re.MULTILINE)
        if name_m:
            bad = {"UCO","BANK","KENDRIYA","VIDYALAY","KOTA","SCHOOL","GALI","NAGAR","BEHIND"}
            words = [w for w in name_m.group(1).split()
                     if w not in bad]
            if words:
                self.summary.account_holder = " ".join(words)

        ob = re.search(r"Opening Balance\s+([\d.]+)", text)
        cb = re.search(r"Closing Balance\s+([\d.]+)", text)
        if ob:
            self.summary.opening_balance = self._to_float(ob.group(1))
        if cb:
            self.summary.closing_balance = self._to_float(cb.group(1))

    def _parse_metadata_generic(self, text):
        acc_m = re.search(r"(?:Account\s*(?:No|Number))[:\s]+(\d{8,18})", text, re.IGNORECASE)
        if acc_m:
            self.summary.account_number = acc_m.group(1)

    # ── Step 4: Parse transactions ───────────────────────────
    def parse_transactions(self):
        if self.bank_format == "sbi":
            self._parse_sbi()
        elif self.bank_format == "uco":
            self._parse_uco()
        else:
            self._parse_uco()  # fallback

        print(f"  Parsed {len(self.summary.transactions)} transactions")

    # ── SBI Parser ───────────────────────────────────────────
    def _parse_sbi(self):
        """
        SBI actual format (WDL/DEP on its own line, data on next line):

        Line N:   'WDL TFR'   or   'DEP TFR'
        Line N+1: 'DD/MM/YYYY DD/MM/YYYY UPI/DR/.../MERCHANT - AMT - BAL'
        Line N+2: '/BANK/handle/...'   (continuation, optional)
        Line N+3: 'REF AT BRANCH'      (ref, skip)
        """
        DATA_RE = re.compile(
            r"^(\d{2}/\d{2}/\d{4})\s+"    # date1
            r"\d{2}/\d{2}/\d{4}\s+"        # date2 (duplicate)
            r"(.+?)\s+"                        # description (lazy)
            r"-\s+"                            # dash separator
            r"([\d,]+\.\d{2}|-)\s+"        # withdrawal or -
            r"-?\s*"                           # optional dash
            r"([\d,]+\.\d{2}|-)\s+"        # deposit or -
            r"([\d,]+\.\d{2})"              # running balance
        )

        lines = self.raw_text.split("\n")
        balances_seen = []
        current_type = None

        for i, line in enumerate(lines):
            stripped = line.strip()

            # Detect WDL/DEP marker line
            if stripped.startswith("WDL"):
                current_type = "debit"
                continue
            if stripped.startswith("DEP"):
                current_type = "credit"
                continue

            # Try to match data line
            m = DATA_RE.match(stripped)
            if not m or current_type is None:
                continue

            date     = m.group(1)
            raw_desc = m.group(2).strip()
            wdl      = m.group(3)
            dep      = m.group(4)
            balance  = self._to_float(m.group(5))

            # Collect continuation line (next line starting with /)
            continuation = ""
            if i + 1 < len(lines):
                nxt = lines[i + 1].strip()
                if nxt.startswith("/"):
                    continuation = nxt

            full_desc = raw_desc + continuation

            merchant = self._extract_merchant(full_desc) or raw_desc[:60]

            # Get amount (whichever is not "-")
            amount = self._to_float(wdl if wdl != "-" else dep)

            # Compute opening balance from first transaction
            if not balances_seen:
                if current_type == "debit":
                    self.summary.opening_balance = round(balance + amount, 2)
                else:
                    self.summary.opening_balance = round(balance - amount, 2)
            balances_seen.append(balance)

            category, sub_cat = self._categorize(merchant + " " + full_desc)
            # If we couldn't classify but it looks like a transfer, label it as such
            if category == "other" and sub_cat == "uncategorized":
                if re.search(r"\bUPI/|IMPS/|NEFT/|RTGS/|ACH/|NACH\b", full_desc, re.IGNORECASE):
                    sub_cat = "transfer"

            self.summary.transactions.append(Transaction(
                date=date,
                description=merchant,
                raw_description=full_desc,
                amount=amount,
                transaction_type=current_type,
                category=category,
                sub_category=sub_cat
            ))

            current_type = None  # reset after consuming data line

        if balances_seen:
            self.summary.closing_balance = balances_seen[-1]

    def _clean_sbi_desc(self, desc: str, full_line: str) -> str:
        """
        Extract merchant name from SBI UPI description.
        UPI/DR/600130602066/Mr ANIL/YESB/q702618131/UPI → Mr ANIL
        UPI/CR/696696487260/NIDHI GA/PUNB/9828858006/Sent → NIDHI GA
        """
        # Try extracting after 3rd slash in UPI ref
        upi_m = re.search(r"UPI/(?:DR|CR)/\d+/([^/]+)", desc, re.IGNORECASE)
        if upi_m:
            name = upi_m.group(1).strip()
            # Remove trailing junk
            name = re.sub(r'\s+\w{4,}\s*$', '', name).strip()
            return name

        # NEFT/IMPS pattern
        neft_m = re.search(r"(?:NEFT|IMFT|IMPS)/\w+/(.+?)(?:/|$)", desc, re.IGNORECASE)
        if neft_m:
            return neft_m.group(1).strip()

        return desc[:60].strip()

    def _extract_merchant(self, full_desc: str) -> str:
        """
        Try to extract a human-readable merchant/person from raw narration.
        Works best for SBI UPI strings, but also handles NEFT/IMPS/POS-ish texts.
        """
        desc = (full_desc or "").strip()
        if not desc:
            return ""

        # UPI patterns
        upi_m = re.search(r"UPI/(?:DR|CR)/\d+/([^/]+)", desc, re.IGNORECASE)
        if upi_m:
            name = upi_m.group(1).strip()
            name = re.sub(r"\s+\w{4,}\s*$", "", name).strip()
            return name[:60]

        # NEFT/IMPS/RTGS patterns: NEFT/XXXX/NAME/...
        bank_m = re.search(r"(?:NEFT|IMFT|IMPS|RTGS)/[^/]+/([^/]+)", desc, re.IGNORECASE)
        if bank_m:
            return bank_m.group(1).strip()[:60]

        # POS / card purchase hints
        pos_m = re.search(r"\bPOS(?:\s*PRCR)?\s+([A-Z0-9][A-Z0-9\s\-]{2,40})", desc, re.IGNORECASE)
        if pos_m:
            return pos_m.group(1).strip()[:60]

        # Fallback: first chunk before amount separators
        return re.split(r"\s+-\s+", desc, maxsplit=1)[0].strip()[:60]

    # ── UCO Parser ───────────────────────────────────────────
    def _parse_uco(self):
        """
        UCO Bank: plain text, 3-line blocks:
        MPAYUPITRTR...MERCHANTNAME...
        DD-Mon-YYYY  amount  balance
        ref/timestamp
        """
        txn_re = re.compile(
            r"^(\d{2}-(?:Jan|Feb|Mar|Apr|May|Jun|Jul|Aug|Sep|Oct|Nov|Dec)-\d{4})"
            r"\s+([\d]+(?:\.\d+)?)"
            r"\s+([\d]+\.\d{2})"
            r"\s*$",
            re.MULTILINE | re.IGNORECASE
        )

        lines = self.raw_text.split("\n")
        prev_balance = self.summary.opening_balance

        for i, line in enumerate(lines):
            m = txn_re.match(line.strip())
            if not m:
                continue

            date    = m.group(1)
            amount  = self._to_float(m.group(2))
            balance = self._to_float(m.group(3))

            txn_type = "credit" if round(balance, 2) > round(prev_balance, 2) else "debit"

            # Description = line above (skip ref ID lines)
            particulars = ""
            if i > 0:
                above = lines[i - 1].strip()
                if re.match(r"^[\d/\-:\s]+$", above):
                    above = lines[i - 2].strip() if i > 1 else ""
                particulars = above

            cleaned_particulars = self._clean_particulars(particulars)
            category, sub_cat = self._categorize(cleaned_particulars)
            if category == "other" and sub_cat == "uncategorized":
                if re.search(r"\bUPI\b|IMPS|NEFT|RTGS|ACH|NACH", particulars, re.IGNORECASE):
                    sub_cat = "transfer"
            self.summary.transactions.append(Transaction(
                date=date,
                description=cleaned_particulars,
                raw_description=particulars,
                amount=amount,
                transaction_type=txn_type,
                category=category,
                sub_category=sub_cat
            ))
            prev_balance = balance

    # ── Categorize ───────────────────────────────────────────
    def _categorize(self, description: str) -> tuple:
        desc = self._normalize_for_rules(description)
        for keyword, (cat, sub) in CATEGORY_RULES.items():
            if self._normalize_for_rules(keyword) in desc:
                return cat, sub
        # Heuristic fallbacks (helps a lot for generic PDFs)
        if "atm" in desc or "cashwithdraw" in desc:
            return "expense", "cash_withdrawal"
        if "pos" in desc or "debitcard" in desc:
            return "expense", "pos_purchase"
        return "other", "uncategorized"

    def _normalize_for_rules(self, s: str) -> str:
        # Lowercase and keep only alphanumerics to match messy narration reliably
        return re.sub(r"[^a-z0-9]+", "", (s or "").lower())

    def _clean_particulars(self, s: str) -> str:
        """
        Strip noisy prefixes so categorization sees the merchant clearly.
        """
        text = (s or "").strip()
        # remove common UCO/UPI prefixes
        text = re.sub(r"^(MPAYUPITRTR|MPAYUPI|UPI|IMPS|NEFT|RTGS)\s*", "", text, flags=re.IGNORECASE)
        # remove long ref-like blobs
        text = re.sub(r"\b\d{10,}\b", "", text).strip()
        return re.sub(r"\s{2,}", " ", text).strip()

    # ── Aggregate ────────────────────────────────────────────
    def compute_summary(self):
        debit_by_sub = defaultdict(float)
        credit_by_sub = defaultdict(float)
        debit_by_cat = defaultdict(float)
        credit_by_cat = defaultdict(float)

        for txn in self.summary.transactions:
            if txn.transaction_type == "credit":
                self.summary.liquid_assets += txn.amount
                credit_by_sub[txn.sub_category or "uncategorized"] += txn.amount
                credit_by_cat[txn.category or "uncategorized"] += txn.amount
                if txn.category == "income":
                    self.summary.total_income += txn.amount
                    if txn.sub_category == "salary":
                        self.summary.salary_income += txn.amount
                    else:
                        self.summary.other_income  += txn.amount

            elif txn.transaction_type == "debit":
                debit_by_sub[txn.sub_category or "uncategorized"] += txn.amount
                debit_by_cat[txn.category or "uncategorized"] += txn.amount
                if txn.category == "investment":
                    self.summary.total_investments += txn.amount
                    if   txn.sub_category == "mutual_fund": self.summary.mutual_funds       += txn.amount
                    elif txn.sub_category == "stocks":      self.summary.stocks             += txn.amount
                    elif txn.sub_category == "fd_rd":       self.summary.fd_rd              += txn.amount
                    elif txn.sub_category == "insurance":   self.summary.insurance_premiums += txn.amount

                elif txn.category == "liability":
                    self.summary.total_liabilities += txn.amount
                    if   txn.sub_category == "loan_emi":    self.summary.loan_emis            += txn.amount
                    elif txn.sub_category == "credit_card": self.summary.credit_card_payments += txn.amount

                elif txn.category in ("expense", "other"):
                    self.summary.total_expenses += txn.amount
                    if txn.sub_category in ESSENTIAL_SUBCATEGORIES:
                        self.summary.essential_expenses     += txn.amount
                    else:
                        self.summary.discretionary_expenses += txn.amount

        if self.summary.total_income > 0:
            net = (self.summary.total_income
                   - self.summary.total_expenses
                   - self.summary.total_investments)
            self.summary.savings_rate         = round(net / self.summary.total_income * 100, 2)
            self.summary.investment_rate      = round(self.summary.total_investments / self.summary.total_income * 100, 2)
            self.summary.debt_to_income_ratio = round(self.summary.total_liabilities / self.summary.total_income * 100, 2)

        # Freeze aggregates into plain dicts for JSON export
        self.summary.debit_by_sub_category = dict(sorted(debit_by_sub.items(), key=lambda x: -x[1]))
        self.summary.credit_by_sub_category = dict(sorted(credit_by_sub.items(), key=lambda x: -x[1]))
        self.summary.debit_by_category = dict(sorted(debit_by_cat.items(), key=lambda x: -x[1]))
        self.summary.credit_by_category = dict(sorted(credit_by_cat.items(), key=lambda x: -x[1]))

    def _to_float(self, value: str) -> float:
        try:
            # Remove currency symbols/words and separators (keep decimal dot)
            s = str(value)
            s = s.replace("₹", "").replace("INR", "").replace("Rs.", "").replace("Rs", "")
            cleaned = re.sub(r"[,\s]", "", s)
            return float(cleaned)
        except (ValueError, TypeError):
            return 0.0

    def run(self) -> FinancialSummary:
        print(f"\nProcessing: {self.pdf_path.name}")
        self.extract_raw_data()
        self.detect_bank()
        self.parse_metadata()
        self.parse_transactions()
        self.compute_summary()
        print("Done.\n")
        return self.summary


