def print_category_breakdown(summary: FinancialSummary, top_n: int | None = None):
    print("\nCREDIT BREAKDOWN (Money received):")
    print("-" * 50)
    credit_items = list((summary.credit_by_sub_category or {}).items())
    if top_n is not None:
        credit_items = credit_items[: max(0, int(top_n))]
    if credit_items:
        for label, total in credit_items:
            pretty = SUBCATEGORY_LABELS.get(label, label.replace("_", " ").title())
            print(f"  {pretty} ({label})".ljust(34) + f"INR {total:>10,.2f}")
    else:
        print("  No credit transactions found.")

    print("\nDEBIT BREAKDOWN (Money spent):")
    print("-" * 50)
    debit_items = list((summary.debit_by_sub_category or {}).items())
    if top_n is not None:
        debit_items = debit_items[: max(0, int(top_n))]
    if debit_items:
        for label, total in debit_items:
            pretty = SUBCATEGORY_LABELS.get(label, label.replace("_", " ").title())
            print(f"  {pretty} ({label})".ljust(34) + f"INR {total:>10,.2f}")
    else:
        print("  No debit transactions found.")


# ── Export ───────────────────────────────────────────────────

def export_json(summary: FinancialSummary, path: str):
    data = asdict(summary)
    data["transactions"] = [asdict(t) for t in summary.transactions]
    with open(path, "w") as f:
        json.dump(data, f, indent=2)
    print(f"JSON saved: {path}")

def export_csv(summary: FinancialSummary, path: str):
    with open(path, "w", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=[
            "date", "description", "amount",
            "transaction_type", "category", "sub_category",
            "raw_description"
        ])
        writer.writeheader()
        for txn in summary.transactions:
            writer.writerow(asdict(txn))
    print(f"CSV saved: {path}")


def main():
    parser = argparse.ArgumentParser(
        prog="wealthscraper",
        description="Parse a bank statement PDF and generate JSON/CSV summaries.",
    )
    parser.add_argument("pdf", help="Path to the bank statement PDF")
    parser.add_argument(
        "--password",
        default="",
        help="PDF password (if protected). If omitted, you will be prompted (up to 3 tries).",
    )
    parser.add_argument("--out-json", default="financial_summary.json", help="Output JSON path")
    parser.add_argument("--out-csv", default="transactions.csv", help="Output CSV path")
    parser.add_argument(
        "--no-breakdown",
        action="store_true",
        help="Skip printing category breakdown tables",
    )
    parser.add_argument(
        "--top",
        type=int,
        default=None,
        help="Only show top N rows in breakdown tables (optional)",
    )
    parser.add_argument(
        "--debug-dump",
        action="store_true",
        help="Print first 120 lines of extracted text for debugging parsing issues",
    )
    args = parser.parse_args()

    pdf_path = Path(args.pdf)
    if not pdf_path.exists():
        raise FileNotFoundError(f"PDF not found: {pdf_path}")

    scraper = BankStatementScraper(str(pdf_path), password=args.password)
    summary = scraper.run()

    if args.debug_dump:
        print("\n--- DEBUG: First 120 lines of extracted text ---")
        for i, line in enumerate(scraper.raw_text.split("\n")[:120]):
            print(f"{i:3}: {line!r}")
        print("--- END DEBUG ---\n")

    print_report(summary)
    if not args.no_breakdown:
        print_category_breakdown(summary, top_n=args.top)

    export_json(summary, args.out_json)
    export_csv(summary, args.out_csv)


if __name__ == "__main__":
    main()